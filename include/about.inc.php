<h2 style="text-align: center;">How it started</h2>
<p>Like other projects we have initiated, this started out as a little project. When the thought of the initiative dropped in his heart, Adedayo Adebayo prayed and sought the face of the Lord to confirm that it was God’s plan and move to change lives and he was the chosen vessel, not some idea conceived in his mind. He was not seeking fame and fortune as he had since learnt that God was his source and sustenance. After some time of building his convictions that this was God’s project put in his hands, he set out knowing God would hold his hand and walk him through the process unto irreversible perfection.
Adedayo Adebayo set out on this project with a few of his like-minded brethren: Bukola Latinwo, Olajide Animashaun, Mary Yissa, Comfort Yissa, Lydia and Serah Yissa.</p>
<p> At the conception of the initiative, they spent time praying, held several meetings, discussed their progress as well as challenges, and by God’s Spirit, proffered solutions, and ideas that effected progress.
Successful projects are known to face challenges and this project was no exception. There were setbacks, discouragements, and other things the devil threw at them. However, this group of God-called people never backed down. They were constantly reminded of God’s Word to them, and as a result, their minds were established with solid confidence.</p>
<p>BUILD GREENIES started out in June, 2020 with a few Junior Secondary School students in Abadina. The founding members would teach the students God’s Word as well as their academic works. The project started with about six students. They were got saved and filled with the Holy Ghost and the student were then taught to use the Eternal Life of God for their academics.</p>
<p>We said it before and we are saying it again; BUILD GREENIES was no exception when it came to challenges, there were challenges!  One of our experiences with these challenges is the unavailability or negligence of our target group. One day, the teaching volunteers came for the class and waited the whole two hours allocated to each class (which held on Mondays, Thursdays, and Fridays), with no student showing up.</p>
<p>Another challenge was venue availability. Classes were very irregular, and sometimes the students did not do the assignments given or did not attend the one-on-one discipleship meetings. Evaluations were made but there were no evident improvements. Fellowships were sometimes cold as some of the students proved difficult. Although the list of challenges seemed endless, these people helped up in prayers and encouraged one another with God’s Word. As always, the prayer of faith- prayer based on God’s Word - did produce good results. Their hearts were strengthened; ideas, strategies and doors opened to them by the workings of God’s Spirit and Power. 
    </p>

    
